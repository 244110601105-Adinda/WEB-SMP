    <script>
        function showToast(message) {
            let toast = document.querySelector('.toast-notification');
            if(toast) toast.remove();
            toast = document.createElement('div');
            toast.className = 'toast-notification';
            toast.innerHTML = message;
            document.body.appendChild(toast);
            setTimeout(() => {
                if(toast) toast.remove();
            }, 2000);
        }
        
        function toggleDropdown(event, button) {
            event.stopPropagation();
            const dropdown = button.closest('.dropdown');
            document.querySelectorAll('.dropdown').forEach(dd => {
                if (dd !== dropdown) dd.classList.remove('show');
            });
            dropdown.classList.toggle('show');
        }
        
        document.addEventListener('click', function(event) {
            document.querySelectorAll('.dropdown').forEach(dropdown => {
                if (!dropdown.contains(event.target)) dropdown.classList.remove('show');
            });
        });
        
        function toggleMobileDropdown(button) {
            button.closest('.mobile-dropdown').classList.toggle('active');
        }
        
        function toggleMobileMenu() {
            const navMenu = document.getElementById('navMenuMobile');
            const overlay = document.getElementById('menuOverlay');
            navMenu.classList.toggle('show');
            overlay.classList.toggle('show');
            document.body.style.overflow = navMenu.classList.contains('show') ? 'hidden' : '';
        }
        
        function closeMobileMenu() {
            document.getElementById('navMenuMobile').classList.remove('show');
            document.getElementById('menuOverlay').classList.remove('show');
            document.body.style.overflow = '';
        }
        
        document.addEventListener('DOMContentLoaded', function() {
            document.body.classList.add('fade-in');
            document.querySelectorAll('#navMenuMobile a, #navMenuMobile .mobile-dropdown-content a').forEach(link => {
                link.addEventListener('click', () => { if (window.innerWidth <= 768) closeMobileMenu(); });
            });
        });
        
        window.addEventListener('resize', function() {
            if (window.innerWidth > 768) {
                document.getElementById('navMenuMobile').classList.remove('show');
                document.getElementById('menuOverlay').classList.remove('show');
                document.body.style.overflow = '';
            }
        });
